// Helper function to get price by treatment slug
function get_treatment_price($treatment_slug) {
    $prices = get_tempus_treatment_prices();
    return isset($prices[$treatment_slug]) ? $prices[$treatment_slug]['price'] : null;
}

// Helper function to get price by partial match (for flexible matching)
function get_treatment_price_fuzzy($treatment_name) {
    $prices = get_tempus_treatment_prices();
    $treatment_name_lower = strtolower($treatment_name);
    
    foreach ($prices as $slug => $price_data) {
        $price_name_lower = strtolower($price_data['name']);
        if (strpos($price_name_lower, $treatment_name_lower) !== false || 
            strpos($treatment_name_lower, $price_name_lower) !== false) {
            return $price_data['price'];
        }
    }
    
    return null;
}